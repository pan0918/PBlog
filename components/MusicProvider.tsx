const MusicContext = createContext<MusicContextValue | null>(null);

// ---------------------------------------------------------------------------
// Provider
// ---------------------------------------------------------------------------

export function MusicProvider({ children }: { children: ReactNode }) {
  const audioRef = useRef<HTMLAudioElement | null>(null);
  const blobUrlRef = useRef<string | null>(null);

  const [currentIndex, setCurrentIndex] = useState(0);
  const [isPlaying, setIsPlaying] = useState(false);
  const [isLoading, setIsLoading] = useState(true);
  const [progress, setProgress] = useState(0);
  const [currentTime, setCurrentTime] = useState(0);
  const [duration, setDuration] = useState(0);
  const [currentLyric, setCurrentLyric] = useState("");
  const [volume, setVolumeState] = useState(1);
  const [isMuted, setIsMuted] = useState(false);
  const [playMode, setPlayMode] = useState<PlayMode>("loop");
  const [parsedLyrics, setParsedLyrics] = useState<LyricLine[]>([]);
  const [audioSrc, setAudioSrc] = useState<string>("");

  const currentSong = SONGS[currentIndex];

  // Clear loading immediately (hardcoded data)
  useEffect(() => { setIsLoading(false); }, []);

  // Clean up blob URL on unmount
  useEffect(() => {
    return () => {
      if (blobUrlRef.current) {
        URL.revokeObjectURL(blobUrlRef.current);
        blobUrlRef.current = null;
      }
      if (audioRef.current) {
        audioRef.current.pause();
        audioRef.current.src = '';
      }
    };
  }, []);

  // Parse lyrics when song changes
  useEffect(() => {
    if (currentSong) setParsedLyrics(parseLrc(currentSong.lrc));
  }, [currentIndex]);

  // Fetch audio as blob — ensures seeking works regardless of CDN range request support
  useEffect(() => {
    if (!currentSong) return;
    let cancelled = false;
    // Reset display states immediately on song change
    setDuration(0);
    setCurrentTime(0);
    setProgress(0);
    setCurrentLyric("♪ 正在缓冲 ♪");
    // Clean up old blob URL
    if (blobUrlRef.current) {
      URL.revokeObjectURL(blobUrlRef.current);
      blobUrlRef.current = null;
    }
    // Phase 1: use direct URL for fast initial playback
    setAudioSrc(currentSong.url);
    // Phase 2: fetch full file in background for reliable seeking
    fetch(currentSong.url)
      .then(res => res.blob())
      .then(blob => {
        if (cancelled) return;
        const url = URL.createObjectURL(blob);
        blobUrlRef.current = url;
        setAudioSrc(url);
      })
      .catch(() => {});
    return () => { cancelled = true; };
  }, [currentIndex]);

  // Sync volume
  useEffect(() => {
    if (audioRef.current) audioRef.current.volume = isMuted ? 0 : volume;
  }, [volume, isMuted]);

  // Time update & lyric sync
  const handleTimeUpdate = () => {
    const audio = audioRef.current;
    if (!audio) return;
    const t = audio.currentTime;
    const dur = audio.duration;
    setCurrentTime(t);
    if (dur && isFinite(dur)) {
      setDuration(dur);
      if (dur > 0) {
        setProgress((t / dur) * 100);
      }
    }
    for (let i = parsedLyrics.length - 1; i >= 0; i--) {
      if (t >= parsedLyrics[i].time) {
        if (parsedLyrics[i].text !== currentLyric) {
          setCurrentLyric(parsedLyrics[i].text);
        }
        break;
      }
    }
  };

  // Get duration immediately when metadata loads (faster than timeupdate)
  const handleLoadedMetadata = () => {
    const audio = audioRef.current;
    if (!audio) return;
    const dur = audio.duration;
    if (dur && isFinite(dur)) {
      setDuration(dur);
    }
  };

  const handleEnded = () => {
    const audio = audioRef.current;
    if (playMode === "single" && audio) {
      audio.currentTime = 0;
      audio.play();
    } else if (playMode === "random") {
      setCurrentIndex(Math.floor(Math.random() * SONGS.length));
    } else {
      setCurrentIndex((prev) => (prev + 1) % SONGS.length);
    }
  };

  // Controls
  const togglePlay = () => {
    const audio = audioRef.current;
    if (!audio) return;
    if (isPlaying) {
      audio.pause();
      setIsPlaying(false);
    } else {
      audio.play().then(() => setIsPlaying(true)).catch(() => {});
    }
  };

  const nextSong = () => setCurrentIndex((prev) => (prev + 1) % SONGS.length);
  const prevSong = () => setCurrentIndex((prev) => (prev - 1 + SONGS.length) % SONGS.length);

  const handleSeek = (time: number) => {
    const audio = audioRef.current;
    if (!audio) return;
    const dur = audio.duration;
    if (!dur || !isFinite(dur)) return;
    setProgress(time);
    audio.currentTime = (time / 100) * dur;
  };

  const playSong = (index: number) => {
    if (index < 0 || index >= SONGS.length) return;
    setCurrentIndex(index);
    const audio = audioRef.current;
    if (audio) {
      audio.play().then(() => setIsPlaying(true)).catch(() => {});
    }
  };

  const setVolume = (v: number) => { setVolumeState(Math.min(1, Math.max(0, v))); setIsMuted(false); };
  const toggleMute = () => setIsMuted((prev) => !prev);
  const togglePlayMode = () => setPlayMode((prev) => prev === "loop" ? "single" : prev === "single" ? "random" : "loop");

  // -- Context value --

  const value: MusicContextValue = {
    playlist: SONGS,
    currentIndex,
    currentSong,
    isPlaying,
    progress,
    currentTime,
    duration,
    currentLyric,
    isLoading,
    volume,
    isMuted,
    playMode,

    togglePlay,
    nextSong,
    prevSong,
    handleSeek,
    playSong,
    setVolume,
    toggleMute,
    togglePlayMode,
  };

  return (
    <MusicContext.Provider value={value}>
      {children}
      {currentSong && audioSrc && (
        <audio
          ref={audioRef}
          src={audioSrc}
          onTimeUpdate={handleTimeUpdate}
          onEnded={handleEnded}
          onLoadedMetadata={handleLoadedMetadata}
          onCanPlay={() => { if (isPlaying && audioRef.current) audioRef.current.play().catch(() => {}); }}
        />
      )}
    </MusicContext.Provider>
  );
}

// ---------------------------------------------------------------------------
// Hook
// ---------------------------------------------------------------------------

export function useMusic(): MusicContextValue {
  const ctx = useContext(MusicContext);
  if (!ctx) {
    throw new Error("useMusic must be used within a MusicProvider");
  }
  return ctx;
}
